<script>
import { mapState } from 'vuex'
export default {
  data () {
    return {
      chartInstance: null,
      allData: null,
      startValue: 0, // åŒºåŸŸç¼©æ”¾çš„èµ·ç‚¹å€¼
      endValue: 9, // åŒºåŸŸç¼©æ”¾çš„ç»ˆç‚¹å€¼
      timerId: null // å®šæ—¶å™¨çš„æ ‡è¯†
    }
  },
  created () {
    // åœ¨ç»„ä»¶åˆ›å»ºå®Œæˆä¹‹å è¿›è¡Œå›è°ƒå‡½æ•°çš„æ³¨å†Œ
    this.$socket.registerCallBack('rankData', this.getData)
  },
  mounted () {
    this.initChart()
    // this.getData()
    this.$socket.send({
      action: 'getData',
      socketType: 'rankData',
      chartName: 'rank',
      value: ''
    })
    window.addEventListener('resize', this.screenAdapter)
    this.screenAdapter()
  },
  destroyed () {
    window.removeEventListener('resize', this.screenAdapter)
    clearInterval(this.timerId)
    this.$socket.unRegisterCallBack('rankData')
  },
  methods: {
    initChart () {
      this.chartInstance = this.$echarts.init(this.$refs.rank_ref, this.theme)
      const initOption = {
        title: {
          text: 'ğŸ’™ åœ°åŒºé”€å”®æ’è¡Œ',
          left: 20,
          top: 20
        },
        grid: {
          top: '40%',
          left: '5%',
          right: '5%',
          bottom: '5%',
          containLabel: true
        },
        tooltip: {
          show: true
        },
        xAxis: {
          type: 'category'
        },
        yAxis: {
          type: 'value'
        },
        series: [
          {
            type: 'bar'
          }
        ]
      }
      this.chartInstance.setOption(initOption)
      this.chartInstance.on('mouseover', () => {
        clearInterval(this.timerId)
      })
      this.chartInstance.on('mouseout', () => {
        this.startInterval()
      })
    },
    getData (ret) {
      // è·å–æœåŠ¡å™¨çš„æ•°æ®, å¯¹this.allDataè¿›è¡Œèµ‹å€¼ä¹‹å, è°ƒç”¨updateChartæ–¹æ³•æ›´æ–°å›¾è¡¨
      // const { data: ret } = await this.$http.get('rank')
      this.allData = ret
      // å¯¹allDataé‡Œé¢çš„æ¯ä¸€ä¸ªå…ƒç´ è¿›è¡Œæ’åº, ä»å¤§åˆ°å°è¿›è¡Œ
      this.allData.sort((a, b) => {
        return b.value - a.value
      })
      console.log(this.allData)
      this.updateChart()
      this.startInterval()
    },
    updateChart () {
      const colorArr = [
        ['#0BA82C', '#4FF778'],
        ['#2E72BF', '#23E5E5'],
        ['#5052EE', '#AB6EE5']
      ]
      // å¤„ç†å›¾è¡¨éœ€è¦çš„æ•°æ®
      // æ‰€æœ‰çœä»½æ‰€å½¢æˆçš„æ•°ç»„
      const provinceArr = this.allData.map(item => {
        return item.name
      })
      // æ‰€æœ‰çœä»½å¯¹åº”çš„é”€å”®é‡‘é¢
      const valueArr = this.allData.map(item => {
        return item.value
      })
      const dataOption = {
        xAxis: {
          data: provinceArr
        },
        dataZoom: {
          show: false,
          startValue: this.startValue,
          endValue: this.endValue
        },
        series: [
          {
            data: valueArr,
            itemStyle: {
              color: arg => {
                let targetColorArr = null
                if (arg.value > 300) {
                  targetColorArr = colorArr[0]
                } else if (arg.value > 200) {
                  targetColorArr = colorArr[1]
                } else {
                  targetColorArr = colorArr[2]
                }
                return new this.$echarts.graphic.LinearGradient(0, 0, 0, 1, [
                  {
                    offset: 0,
                    color: targetColorArr[0]
                  },
                  {
                    offset: 1,
                    color: targetColorArr[1]
                  }
                ])
              }
            }
          }
        ]
      }
      this.chartInstance.setOption(dataOption)
    },
    screenAdapter () {
      const titleFontSize = this.$refs.rank_ref.offsetWidth / 100 * 3.6
      const adapterOption = {
        title: {
          textStyle: {
            fontSize: titleFontSize
          }
        },
        series: [
          {
            barWidth: titleFontSize,
            itemStyle: {
              barBorderRadius: [titleFontSize / 2, titleFontSize / 2, 0, 0]
            }
          }
        ]
      }
      this.chartInstance.setOption(adapterOption)
      this.chartInstance.resize()
    },
    startInterval () {
      if (this.timerId) {
        clearInterval(this.timerId)
      }
      this.timerId = setInterval(() => {
        this.startValue++
        this.endValue++
        if (this.endValue > this.allData.length - 1) {
          this.startValue = 0
          this.endValue = 9
        }
        this.updateChart()
      }, 2000)
    }
  },
  computed: {
    ...mapState(['theme'])
  },
  watch: {
    theme () {
      console.log('ä¸»é¢˜åˆ‡æ¢äº†')
      this.chartInstance.dispose() // é”€æ¯å½“å‰çš„å›¾è¡¨
      this.initChart() // é‡æ–°ä»¥æœ€æ–°çš„ä¸»é¢˜åç§°åˆå§‹åŒ–å›¾è¡¨å¯¹è±¡
      this.screenAdapter() // å®Œæˆå±å¹•çš„é€‚é…
      this.updateChart() // æ›´æ–°å›¾è¡¨çš„å±•ç¤º
    }
  }
}
</script>

